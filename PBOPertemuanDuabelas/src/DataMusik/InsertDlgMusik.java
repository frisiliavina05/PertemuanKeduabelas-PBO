    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (tfIdLagu.getText().equals("") || tfIdAlbum.getText().equals("")
                || tfJudul.getText().equals("") || tfArtis.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Isi semua data terlebih dahulu!");
        } else {
            String idMusik = tfIdLagu.getText();
            String idAlbum = tfIdAlbum.getText();
            String judul = tfJudul.getText();
            String artis = tfArtis.getText();

            try {
                // Buat koneksi persistence
                EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBOPertemuanDuabelasPU");
                EntityManager em = emf.createEntityManager();
                em.getTransaction().begin();

                // ðŸ”¹ Ambil entitas album berdasarkan ID (karena FK)
                Album album = em.find(Album.class, idAlbum);
                if (album == null) {
                    JOptionPane.showMessageDialog(null,
                            "ID Album tidak ditemukan di database!",
                            "Error", JOptionPane.ERROR_MESSAGE);
                    em.close();
                    emf.close();
                    return;
                }

                // ðŸ”¹ Validasi 1 â€” Cek apakah ID Lagu sudah digunakan (Primary Key)
                Musik existing = em.find(Musik.class, idMusik);
                if (existing != null) {
                    JOptionPane.showMessageDialog(null,
                            "ID Lagu sudah digunakan! Gunakan ID lain.",
                            "Duplikat ID Lagu", JOptionPane.WARNING_MESSAGE);
                    em.close();
                    emf.close();
                    return;
                }

                // ðŸ”¹ Validasi 2 â€” Cek apakah lagu dengan judul & artis sama sudah ada di album yang sama
                Query q = em.createQuery(
                        "SELECT COUNT(m) FROM Musik m WHERE m.judul = :judul AND m.artis = :artis AND m.idAlbum = :album"
                );
                q.setParameter("judul", judul);
                q.setParameter("artis", artis);
                q.setParameter("album", album);
                long count = (long) q.getSingleResult();

                if (count > 0) {
                    JOptionPane.showMessageDialog(null,
                            "Lagu dengan judul dan artis yang sama sudah ada di album ini!",
                            "Duplikat Lagu", JOptionPane.WARNING_MESSAGE);
                    em.close();
                    emf.close();
                    return;
                }

                // Buat object JadwalTayang
                Musik musik = new Musik();
                musik.setIdMusik(idMusik);
                musik.setIdAlbum(album);   // relasi FK ke entitas Album
                musik.setJudul(judul);
                musik.setArtis(artis);

                // Simpan ke database
                em.persist(musik);
                em.getTransaction().commit();

                JOptionPane.showMessageDialog(null, "Data musik berhasil disimpan!");

                // Bersihkan field input
                bersih();

                // Refresh tabel di JFrame (pastikan ada method showTableJadwal di JFrame)
                utama.showTableMusik();

                em.close();
                emf.close();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,
                        "Terjadi kesalahan saat menyimpan data: " + e.getMessage(),
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

