    private void btnUploadMusikActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadMusikActionPerformed
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int returnValue = jfc.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File filePilihan = jfc.getSelectedFile();
            System.out.println("yang dipilih : " + filePilihan.getAbsolutePath());

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBOPertemuanDuabelasPU");
            EntityManager em = emf.createEntityManager();

            try (BufferedReader br = new BufferedReader(new FileReader(filePilihan))) {
                String baris;
                String pemisah = ";";

                em.getTransaction().begin();

                while ((baris = br.readLine()) != null) {
                    String[] data = baris.split(pemisah);

                    // validasi data minimal 4 kolom
                    if (data.length < 4) {
                        continue;
                    }

                    String idMusik = data[0].trim();
                    String idAlbum = data[1].trim();
                    String judul = data[2].trim();
                    String artis = data[3].trim();

                    // validasi tidak kosong
                    if (!idMusik.isEmpty() && !idAlbum.isEmpty() && !judul.isEmpty()
                            && !artis.isEmpty()) {

                        // buat objek musik
                        Musik m = new Musik();
                        m.setIdMusik(idMusik);
                        m.setJudul(judul);
                        m.setArtis(artis);

                        // ambil objek Album dari database (FK)
                        Album a = em.find(Album.class, idAlbum);
                        if (a != null) {
                            m.setIdAlbum(a);
                        } else {
                            JOptionPane.showMessageDialog(null, "ID Musik tidak ditemukan: " + idMusik);
                            continue;
                        }

                        // simpan
                        em.persist(m);
                    } else {
                        JOptionPane.showMessageDialog(null, "Data tidak lengkap: " + baris);
                    }
                }

                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Data Musik berhasil diinput!");
                showTableMusik(); // pastikan method ini ada

            } catch (FileNotFoundException ex) {
                Logger.getLogger(Utama.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "File tidak ditemukan");
            } catch (IOException ex) {
                Logger.getLogger(Utama.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Gagal membaca file");
            } catch (Exception ex) {
                if (em.getTransaction().isActive()) {
                    em.getTransaction().rollback();
                }
                Logger.getLogger(Utama.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat upload");
            } finally {
                em.close();
                emf.close();
            }
        }
    }//GEN-LAST:event_btnUploadMusikActionPerformed

