    private void btnUploadAlbumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadAlbumActionPerformed
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int returnValue = jfc.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File filePilihan = jfc.getSelectedFile();
            System.out.println("yang dipilih : " + filePilihan.getAbsolutePath());

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBOPertemuanDuabelasPU");
            EntityManager em = emf.createEntityManager();

            try (BufferedReader br = new BufferedReader(new FileReader(filePilihan))) {
                String baris;
                String pemisah = ";";

                em.getTransaction().begin();

                while ((baris = br.readLine()) != null) {
                    String[] data = baris.split(pemisah);

                    // validasi data
                    if (data.length < 4) {
                        continue;
                    }

                    String idAlbum = data[0].trim();
                    String nama = data[1].trim();
                    String genre = data[2].trim();
                    int tahun = Integer.parseInt(data[3].trim());

                    if (!idAlbum.isEmpty() && !nama.isEmpty() && !genre.isEmpty()) {
                        Album a = new Album();
                        a.setIdAlbum(idAlbum);
                        a.setNamaAlbum(nama);
                        a.setGenre(genre);
                        a.setTahunRilis(tahun);

                        em.persist(a);
                    } else {
                        JOptionPane.showMessageDialog(null, "Data tidak lengkap: " + baris);
                    }
                }

                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Sukses diinput");
                showTableAlbum();

            } catch (FileNotFoundException ex) {
                Logger.getLogger(Utama.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "File tidak ditemukan");
            } catch (IOException ex) {
                Logger.getLogger(Utama.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Gagal membaca file");
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Format harga tidak valid");
            } catch (Exception ex) {
                if (em.getTransaction().isActive()) {
                    em.getTransaction().rollback();
                }
                Logger.getLogger(Utama.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat upload");
            } finally {
                em.close();
                emf.close();
            }
        }
    }//GEN-LAST:event_btnUploadAlbumActionPerformed

